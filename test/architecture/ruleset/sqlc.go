// 이 파일은 "sqlc 코드 생성 규칙"을 검사한다.
//
// sqlc란?
//
//	sqlc는 SQL 쿼리 파일(.sql)을 읽어서 Go 코드를 자동 생성해주는 도구다.
//	NestJS/TypeScript 생태계의 Prisma와 비슷한 역할을 한다.
//	차이점: Prisma는 스키마 파일에서 모델+쿼리를 자동 생성하지만,
//	sqlc는 개발자가 직접 작성한 SQL 쿼리를 Go 코드로 변환한다.
//	즉, SQL을 직접 제어하면서도 타입 안전한 코드를 얻을 수 있다.
//
// sqlc.yaml이란?
//
//	프로젝트 루트에 있는 sqlc의 설정 파일이다.
//	어떤 디렉토리의 .sql 파일을 읽어서, 어디에 Go 코드를 생성할지 정의한다.
//	도메인을 추가할 때마다 sqlc.yaml에 새 항목을 등록해야 한다.
//
// 이 파일에서 검사하는 3가지 규칙:
//
//  1. sqlc/missing-sqlc-entry (ERROR)
//     - repo/ 디렉토리에 .sql 파일이 있지만, sqlc.yaml에 해당 경로가 등록되지 않은 경우
//     - .sql 파일이 있다는 것은 sqlc를 쓰겠다는 의도인데, 설정이 누락된 것이다
//
//  2. sqlc/manual-code-in-sqlc-repo (ERROR)
//     - sqlc를 사용하는 repo/ 디렉토리에 수기 작성된 .go 파일이 있는 경우
//     - sqlc가 생성한 파일에는 반드시 "Code generated by sqlc. DO NOT EDIT." 헤더가 있다
//     - 이 헤더가 없는 .go 파일은 개발자가 직접 작성한 것이므로 위반이다
//     - repo/의 Go 코드는 모두 sqlc가 자동 생성해야 한다. 비즈니스 로직은 svc/에 작성.
//
//  3. sqlc/orphan-sqlc-entry (WARNING)
//     - sqlc.yaml에 항목이 있지만 해당 디렉토리에 .sql 파일이 없는 경우
//     - 도메인이 삭제되었지만 sqlc.yaml에서 항목을 빼지 않은 것일 수 있다
package ruleset

