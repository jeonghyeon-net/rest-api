// sqlc.yaml 파싱 헬퍼
// ──────────────────────────────────────────────

// parseSqlcYaml은 프로젝트 루트의 sqlc.yaml 파일을 읽어서 sqlcConfig 구조체로 변환한다.
//
// Go에서 YAML 파싱 흐름:
//  1. os.ReadFile로 파일 전체를 바이트 슬라이스([]byte)로 읽는다
//  2. yaml.Unmarshal로 바이트 슬라이스를 구조체로 변환한다 (역직렬화)
//
// NestJS 비유:
//
//	const content = fs.readFileSync("sqlc.yaml", "utf-8");
//	const config = yaml.parse(content);  // js-yaml 사용
//
// Go에서는 yaml.Unmarshal이 구조체 태그(`yaml:"..."`)를 보고
// YAML 키와 Go 필드를 자동으로 매핑해준다.
func parseSqlcYaml(projectRoot string) (*sqlcConfig, error) {
	// os.ReadFile: 파일 전체를 한 번에 메모리로 읽는다.
	// 작은 설정 파일이므로 전체를 한 번에 읽어도 문제없다.
	data, err := os.ReadFile(filepath.Join(projectRoot, "sqlc.yaml"))
	if err != nil {
		return nil, fmt.Errorf("sqlc.yaml 읽기 실패: %w", err)
	}

	var cfg sqlcConfig
	// yaml.Unmarshal: YAML 바이트를 Go 구조체로 역직렬화한다.
	// &cfg로 포인터를 전달해야 함수 내부에서 값을 채울 수 있다.
	// (Go는 기본적으로 값 복사이므로, 원본을 수정하려면 포인터가 필요하다)
	if err := yaml.Unmarshal(data, &cfg); err != nil {
		return nil, fmt.Errorf("sqlc.yaml 파싱 실패: %w", err)
	}
	return &cfg, nil
}

// ──────────────────────────────────────────────
// .sql 파일이 있는 repo/ 디렉토리 탐색
// ──────────────────────────────────────────────

// findSQLRepoPaths는 internal/domain/ 아래의 모든 repo/ 디렉토리를 탐색하여
// .sql 파일이 하나라도 있는 디렉토리의 절대 경로 목록을 반환한다.
//
// filepath.WalkDir 패턴 설명:
//
//	filepath.WalkDir는 디렉토리 트리를 재귀적으로 순회하는 Go 표준 라이브러리 함수다.
//	Node.js의 fs.readdirSync + 재귀 호출을 하나의 함수로 합쳐놓은 것과 비슷하다.
//
//	인자로 "콜백 함수"를 받아서, 발견되는 모든 파일/디렉토리에 대해 호출한다.
//	콜백이 filepath.SkipDir를 반환하면 해당 디렉토리의 하위 항목은 건너뛴다.
//
//	순회 순서: 깊이 우선(DFS), 알파벳 순
//
// 탐색 전략:
//
//	모든 파일을 하나하나 확인하는 대신, "repo"라는 이름의 디렉토리를 찾아서
//	그 안에 .sql 파일이 있는지만 확인한다. 효율적으로 탐색하기 위해
//	repo/ 디렉토리를 찾으면 filepath.SkipDir로 더 깊이 들어가지 않는다.
func findSQLRepoPaths(projectRoot string) []string {
	var result []string

	domainRoot := filepath.Join(projectRoot, "internal", "domain")
	// internal/domain/ 디렉토리가 없으면 (프로젝트 초기) 빈 결과 반환
	if _, err := os.Stat(domainRoot); os.IsNotExist(err) {
		return nil
	}

	// filepath.WalkDir로 domainRoot 아래를 재귀 순회한다.
	// 콜백 함수의 파라미터:
	//   path: 현재 항목의 전체 경로
	//   d: 디렉토리 엔트리 (이름, 타입 등)
	//   err: 이 항목에 접근할 때 발생한 에러 (권한 문제 등)
	//
	// 반환값:
	//   nil → 순회 계속
	//   filepath.SkipDir → 이 디렉토리의 하위 항목 건너뛰기
	//   그 외 에러 → 순회 중단
	//nolint:errcheck,gosec // WalkDir 콜백 내에서 에러를 개별 처리한다 (건너뛰기).
	filepath.WalkDir(domainRoot, func(path string, d os.DirEntry, err error) error {
		if err != nil {
			return nil //nolint:nilerr // 접근 에러가 발생한 항목은 건너뛰고 순회를 계속한다.
		}

		// 디렉토리가 아니거나, 이름이 "repo"가 아니면 건너뛴다
		if !d.IsDir() || d.Name() != "repo" {
			return nil
		}

		// "repo" 디렉토리를 찾았으니, 안에 .sql 파일이 있는지 확인한다
		entries, readErr := os.ReadDir(path)
		if readErr != nil {
			return nil //nolint:nilerr // 읽기 실패한 디렉토리는 건너뛰고 순회를 계속한다.
		}
		for _, entry := range entries {
			// .sql 확장자를 가진 파일이 하나라도 있으면 이 디렉토리를 결과에 추가
			if !entry.IsDir() && strings.HasSuffix(entry.Name(), ".sql") {
				result = append(result, path)
				// filepath.SkipDir: 이 repo/ 안의 하위 디렉토리는 탐색할 필요 없음
				return filepath.SkipDir
			}
		}
		return nil
	})

	return result
}

// ──────────────────────────────────────────────
// 수기 작성 Go 파일 검사
// ──────────────────────────────────────────────

// checkManualGoFiles는 .sql 파일이 있는 repo/ 디렉토리의 모든 .go 파일을 검사하여
// sqlc가 자동 생성한 파일이 아닌 수기 작성 파일을 찾아낸다.
//
// sqlc가 생성한 Go 파일에는 항상 다음 헤더가 포함된다:
//
//	// Code generated by sqlc. DO NOT EDIT.
//
// 이 헤더가 없는 .go 파일은 개발자가 직접 작성한 것이므로 위반으로 보고한다.
//
// 왜 수기 Go 파일을 금지하는가?
//
//	sqlc가 관리하는 repo/ 디렉토리에 수동으로 Go 파일을 추가하면,
//	sqlc가 코드를 재생성할 때 충돌이 발생하거나, 수기 코드가 덮어씌워질 수 있다.
//	비즈니스 로직은 svc/ 레이어에 작성해야 한다.
func checkManualGoFiles(repoPath string) []report.Violation {
	var violations []report.Violation

	entries, err := os.ReadDir(repoPath)
	if err != nil {
		return nil
	}

	for _, entry := range entries {
		// 디렉토리이거나 .go 파일이 아니면 건너뛴다
		if entry.IsDir() || !strings.HasSuffix(entry.Name(), ".go") {
			continue
		}

		filePath := filepath.Join(repoPath, entry.Name())
		// sqlc 자동 생성 헤더가 없으면 → 수기 작성 파일
		if !hasSqlcGeneratedHeader(filePath) {
			violations = append(violations, report.Violation{
				Rule:     "sqlc/manual-code-in-sqlc-repo",
				Severity: report.Error,
				Message:  "sqlc를 사용하는 repo/ 디렉토리에 수기 작성된 Go 파일 발견: " + entry.Name(),
				File:     filePath,
				Fix:      "repo/의 Go 코드는 sqlc가 자동 생성해야 한다. 비즈니스 로직은 svc/ 레이어에 작성",
			})
		}
	}

	return violations
}

// ──────────────────────────────────────────────
// 생성 파일 헤더 확인
// ──────────────────────────────────────────────

// hasSqlcGeneratedHeader는 파일의 처음 5줄 안에 sqlc 자동 생성 헤더가 있는지 확인한다.
//
// Go의 코드 생성 컨벤션:
//
//	Go 생태계에서는 도구가 자동 생성한 파일의 상단에 특정 주석을 넣는 관례가 있다.
//	예: "// Code generated by <도구명>. DO NOT EDIT."
//	이 관례는 go generate 문서에도 명시되어 있다.
//	golangci-lint 같은 린터도 이 주석이 있으면 해당 파일의 린트를 건너뛴다.
//
// 왜 5줄만 확인하는가?
//
//	sqlc가 생성하는 파일의 헤더 형태:
//	  // Code generated by sqlc. DO NOT EDIT.
//	  // versions:
//	  //   sqlc v1.xx.x
//	  package repo
//	헤더는 항상 파일 상단에 있으므로, 처음 5줄이면 충분하다.
//	파일 전체를 읽지 않아 성능상 이점이 있다.
//
// defer f.Close() 패턴:
//
//	Go에서 파일을 열면 반드시 닫아야 한다 (리소스 누수 방지).
//	defer 키워드를 쓰면, 현재 함수가 종료될 때 자동으로 실행된다.
//	NestJS의 finally 블록과 비슷하지만, 선언 위치가 더 자유롭다.
//	예: defer f.Close()  → 이 함수가 return될 때 f.Close()가 호출됨
func hasSqlcGeneratedHeader(filePath string) bool {
	file, err := os.Open(filePath)
	if err != nil {
		return false // 파일을 열 수 없으면 헤더 없는 것으로 간주
	}
	// defer: 이 함수가 끝날 때(어떤 경로로 return하든) file.Close()를 실행한다.
	// _ = file.Close()에서 _는 에러 반환값을 무시하는 Go 관용 표현이다.
	defer func() { _ = file.Close() }()

	// bufio.Scanner: 파일을 줄 단위로 효율적으로 읽는 도구다.
	// 내부적으로 버퍼를 사용해서 디스크 I/O 횟수를 줄인다.
	// Node.js의 readline.createInterface와 비슷하다.
	scanner := bufio.NewScanner(file)
	// sqlcHeaderMaxLines는 sqlc 생성 헤더를 검색할 최대 줄 수다.
	// sqlc 헤더는 항상 파일 상단에 위치하므로 5줄이면 충분하다.
	const sqlcHeaderMaxLines = 5

	lineCount := 0
	for scanner.Scan() {
		lineCount++
		if lineCount > sqlcHeaderMaxLines {
			break // 5줄을 넘으면 더 이상 확인할 필요 없다
		}
		// strings.Contains: 문자열 안에 특정 부분 문자열이 있는지 확인한다.
		// JavaScript의 str.includes()와 동일하다.
		if strings.Contains(scanner.Text(), "Code generated by sqlc. DO NOT EDIT.") {
			return true
		}
	}
	return false
}
